      color: "#c1a8f4",
      opacity: 0.8
    })
    circos.chords('lines_5', lines_5, {
      color: "#7d5be8",
      opacity: 0.8
    })
    circos.chords('lines_6', lines_6, {
      color: "#422ddf",
      opacity: 0.8
    })

    circos.render();
