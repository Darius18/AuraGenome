      if (item["color"] >= 100 && item["color"] <= 150) {
        return true;
      } else {
        return false;
      }
    })
    let lines_4 = lines.filter((item) => {
      if (item["color"] >= 150 && item["color"] <= 200) {
        return true;
      } else {
        return false;
      }
    })
